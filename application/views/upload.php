$(function () {
    'use strict';
    // Initialize the jQuery File Upload widget:
    $('#fileupload<?=$id?>').fileupload({
        // Uncomment the following to send cross-domain cookies:
        //xhrFields: {withCredentials: true},
        url: 'upload/upload_file/<?php echo (!empty($directory)) ? "$directory" : 0 ?>/<?php echo (!empty($subdirectory)) ? "$subdirectory" : 0 ?>/<?php echo (!empty($document_id)) ? "$document_id" : 0?>/<?php if (!empty($module)) echo $module?>',
        uploadTemplateId: 'template-upload<?=$id?>',
        downloadTemplateId: 'template-download<?=$id?>',
        autoUpload: true
    });

    // Enable iframe cross-domain access via redirect option:
    $('#fileupload<?=$id?>').fileupload(
        'option',
        'redirect',
        window.location.href.replace(
            /\/[^\/]*$/,
            '/cors/result.html?%s'
        )
    );

    // Load existing files:
    $('#fileupload<?=$id?>').addClass('fileupload-processing');
    $.ajax({
        // Uncomment the following to send cross-domain cookies:
        //xhrFields: {withCredentials: true},
        url: $('#fileupload<?=$id?>').fileupload('option', 'url'),
        dataType: 'json',
        context: $('#fileupload<?=$id?>')[0],
        type: 'POST'
    }).always(function () {
        $(this).removeClass('fileupload-processing');
    }).done(function (result) {
        $(this).fileupload('option', 'done')
            .call(this, $.Event('done'), {result: result});
    });

});

